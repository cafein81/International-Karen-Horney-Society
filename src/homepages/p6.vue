          <v-card-title class="titl">More Information</v-card-title>
          <v-card-text class="cont">
            Karen Horney's thought went through three phases: in the 1920s and early 1930s, she wrote a series of essays in
            which she tried to modify orthodox ideas about feminine psychology while staying within the framework of Freudian
            theory. In The Neurotic Personality of Our Time (New York, 1937) and New Ways in Psychoanalysis (New York, 1939),
            she tried to redefine psychoanalysis by replacing Freud's biological orientation with an emphasis on culture and
            interpersonal relationships. In Our Inner Conflicts (New York, 1945) and Neurosis and Human Growth (New York, 1950),
            she developed her mature theory in which individuals cope with the anxiety produced by feeling unsafe, unloved, and
            unvalued by disowning their spontaneous feelings and developing elaborate strategies of defense.
          </v-card-text>
          <v-card-title class="h4">Feminine Psychology</v-card-title>

          <v-card-text class="cont">
            During her lifetime, Horney and her work were well known, but after her death, her influence gradually declined.
            A revival of interest began with the publication of Feminine Psychology (New York, 1967), a collection of her essays
            from the 1920s and 30s, many of which were originally written in German. Disagreeing with Freud about penis envy,
            female masochism, and feminine development, these essays were controversial when they first appeared but then were
            largely ignored until they were collected in 1967. They have been widely read since, and there has been a growing
            recognition that Karen Horney was the first great psychoanalytic feminist.
          </v-card-text>
          <v-card-text class="cont">
            In her essays on feminine psychology, Horney strove to show that females have intrinsic biological constitutions
            and patterns of development that are to be understood in their own terms and not just as products of their difference
            from males. She argued that psychoanalysis regards women as defective men because it is the product of a male genius
            (Freud) and a male-dominated culture. The male view of the female has been incorporated into psychoanalysis as a
            scientific picture of woman's essential nature. Horney developed the concept of "womb-envy," contending that male
            envy of pregnancy, childbirth, and motherhood, and of the breasts and suckling, gives rise to an unconscious tendency
            to devalue women. Men's impulse toward creative work is an overcompensation, she argued, for their small role in procreation.
          </v-card-text>

          <v-card-title class="h4">Second Phase of Horney's thought</v-card-title>
          <v-card-text class="cont">
            In the second phase of Horney's thought, culture and disturbed human relationships replaced biology as the most important
            causes of neurotic development. As the author of The Neurotic Personality of Our Time and New Ways in Psychoanalysis,
            Horney is often thought of as a neo-Freudian member of "the cultural school," a group that also includes Erich Fromm,
            Harry Stack Sullivan, Clara Thompson, and Abraham Kardiner. The systematic critique of Freud in New Ways in Psychoanalysis
            aroused so much resentment at the New York Psychoanalytic Institute that Horney was forced to resign.
          </v-card-text>
          <v-card-text class="cont">
            Horney's first two books proposed a model for the structure of neurosis in which adverse conditions in the environment as
            a whole, and especially in the family, create a "basic anxiety" against which the child defends itself by developing strategies
            of defense that are self-alienating, self-defeating, and in conflict with each other. In a striking departure from Freud,
            Horney advocated starting with the current constellation of defenses and inner conflicts rather than with early experience.
            Our problems are the result of past experiences to be sure, but these produce a character structure with an inner logic of
            its own that can be understood without reference to infantile origins. Horney's focus on the present rather than the past
            has led some analysts to complain that her explanations lack depth, while others feel that it is the source of her originality and power.
          </v-card-text>
          <v-card-text class="cont">
            In her next book, Self-Analysis (New York, 1942), Horney presented her fullest account of how the psychoanalytic process works
            in terms of her structural paradigm. The object of therapy for Horney is to help people relinquish their defenses--which alienate
            them from their true likes and dislikes, hopes, fears, and desires--so that they can get in touch with what she called the "real self."
            Self-Analysis contains Horney's only extended case history, that of a patient named Clare, which is highly autobiographical.
            The account of Clare's problems with Peter, her lover, reflect the breakdown of Horney's relationship with Erich Fromm.
            There are chapters on the therapeutic process in several of Horney's other books, and her essays on the topic and the lectures
            she delivered in the last years of her life are collected in The Therapeutic Process (1999).
          </v-card-text>

          <v-card-title class="h4">Mature Theory</v-card-title>
          <v-card-text class="cont">
            In her mature theory, developed in her last two books, Horney retained the emphasis on the present and the basic conception of
            the structure of neurosis developed in earlier works, but she described the defenses and the relationships between them much more
            systematically. According to Horney, people defend themselves against feeling unsafe, unloved, and unvalued by developing both
            interpersonal and intrapsychic neurotic strategies. The interpersonal strategies involve moving toward, against, or away from
            other people and adopting a self-effacing, expansive, or resigned solution. Each of these solutions entails a constellation of
            personality traits, behaviors, and beliefs, and a bargain with fate in which obedience to the dictates of that solution is
            supposed to be rewarded. Since people tend to employ more than one of these strategies, they are beset by inner conflicts.
            In order to avoid being torn apart or paralyzed, they make that strategy predominant which most accords with their culture,
            temperament, and circumstances; but the repressed tendencies persist, generating inconsistencies and rising to the surface if
            the predominant solution fails.
          </v-card-text>
          <v-card-text class="cont">
            In the self-effacing solution, individuals try to gain safety, love, and esteem through dependency, humility, and self-sacrificing
            "goodness." Their bargain is that if they are helpful, submissive people who do not seek their own gain or glory, they will be
            well treated by fate and other people. There are three expansive solutions: the narcissistic, the perfectionistic, and the
            arrogant-vindictive. Narcissists are full of self-admiration, have an unquestioned belief in their own greatness, and often
            display unusual charm and buoyancy. Their magic bargain is that if they hold onto their exaggerated claims for themselves,
            life is bound to give them what they want. Perfectionists take great pride in their rectitude and strive for excellence in
            every detail. They have a legalistic bargain in which correctness of conduct ensures fair treatment by fate and their fellows.
            Arrogant-vindictive people have a need to retaliate for injuries received in childhood and to achieve mastery by manipulating
            others. They do not count on life to give them anything but are convinced that they can reach their ambitious goals if they
            remain true to their vision of the world as a jungle and do not allow themselves to be influenced by their softer feelings or
            the traditional morality. Resigned people worship freedom, peace, and self-sufficiency. Their bargain is that if they ask nothing
            of others, they will not be bothered; that if they try for nothing, they will not fail; and that if they expect little of life,
            they will not be disappointed.
          </v-card-text>
          <v-card-text class="cont">
            The intrapsychic strategies of defense are linked to the interpersonal. To compensate for feelings of weakness, inadequacy,
            and low self-esteem, people develop an idealized image of themselves that they seek to actualize by embarking on a search for glory.
            The idealized image generates a pride system, which consists of neurotic pride, neurotic claims, and tyrannical shoulds. People
            take pride in the imaginary attributes of their idealized selves, they demand that the world treat them in accordance with their
            grandiose conception of themselves, and they drive themselves to live up to the dictates of their solution. The pride system tends
            to intensify the self-hate against which it is supposed to be a defense, since any failure to live up to one's shoulds or of
            the world to honor one's claims leads to feelings of worthlessness. The content of the idealized image is most strongly determined
            by the predominant interpersonal strategy, but since the subordinate strategies are also at work, the idealized image is full of
            inner divisions. As a result, people are often caught in what Horney calls a crossfire of conflicting shoulds. Since obeying the
            dictates of one solution means violating those of another, they are bound to hate themselves whatever they do.
          </v-card-text>
          <v-card-text class="cont">
            Although Horney provides an analytic approach that can be found nowhere else, she deals with many of the same issues as other
            post-Freudians. Her "basic anxiety" is similar to Erik Erikson's "basic mistrust," and her theory illuminates many of the stages
            of development Erikson describes. The search for identity often involves the formation of an idealized image, and there is a
            crisis later in life when people realize that they cannot actualize their idealized image.
          </v-card-text>

          <v-card-title class="h4">Influence</v-card-title>
          <v-card-text class="cont">
            Like Heinz Kohut and his fellows, Horney is interested in problems of the self; and like Harry Guntrip, W. R. D. Fairbairn,
            D. W. Winnicott, John Bowlby, and other members of the British Independent School, she sees neurosis as a product of disturbed
            object-relations, especially in childhood. She differs from self-psychologists in seeing narcissism as reactive rather than
            primary and from object relations theorists in her focus on present structure rather than infantile origins. Horney's
            "real self" bears some resemblance to Kohut's "nuclear self" and even more to Winnicott's "true self." Alice Miller's discussion
            of the loss of and search for the true self in childhood often sounds like Karen Horney, as does R. D. Laing's account of
            ontological insecurity (which is comparable to basic anxiety) and the development of a false-self system in response to it.
          </v-card-text>
          <v-card-text class="cont">
            Horney's mature theory has helped to inspire the interpersonal school of psychoanalysis, it has provided a model for therapies
            that focus on the current situation, and it has influenced some of the descriptions of personality disorders in the Diagnostic
            and Statistical Manual of Mental Disorders (DSM-IV) (Washington, D.C., 1994). It has made an important contribution to the
            study of literature, biography, gender, and culture. Because of her emphasis on self-realization as the goal of life and the
            source of healthy values, Horney was recognized by Abraham Maslow as one of the founders of humanistic psychology. Her theory
            has most in common, perhaps, with the work of Erich Fromm, Ernest Schachtel, Carl Rogers, and Maslow. Many of Horney's ideas
            have made their way, often unacknowledged, into the array of concepts and techniques that are currently employed in clinical practice.
          </v-card-text>
